var dia = 1;
var r = '';

switch(dia){

    case 1 : r = 'Domingo' ; break;
    case 2 : r = 'Segunda' ; break;
    case 3 : r = 'Terça' ; break;
    case 4 : r = 'Quarta' ; break;
    case 5 : r = 'Quinta' ; break;
    case 6 : r = 'Sexta' ; break;
    case 7 : r = 'Sábado' ; break;
    
    default : r = 'Dia inválido!';


}

console.log(r);